type MessageKey = keyof typeof messages.IT;

type I18nContextValue = {
  language: AppLanguage;
  setAppLanguage: (language: string) => void;
  t: (key: MessageKey, values?: Record<string, string | number>) => string;
};

const I18nContext = createContext<I18nContextValue | null>(null);

function normalizeLanguage(value: string | null | undefined): AppLanguage {
  return SUPPORTED_APP_LANGUAGES.includes(value as AppLanguage) ? (value as AppLanguage) : "IT";
}

function interpolate(template: string, values: Record<string, string | number> = {}): string {
  return template
    .replace(/\{(\w+), plural, one \{([^{}]*)\} other \{([^{}]*)\}\}/g, (_match, key, one, other) =>
      Number(values[key]) === 1 ? one : other
    )
    .replace(/\{(\w+)\}/g, (_match, key) => String(values[key] ?? ""));
}

export function I18nProvider({ children }: { children: ReactNode }) {
  const [language, setLanguage] = useState<AppLanguage>("IT");

  useEffect(() => {
    getSettings()
      .then((settings) => setLanguage(normalizeLanguage(settings.language)))
      .catch(() => setLanguage("IT"));
  }, []);

  const value = useMemo<I18nContextValue>(
    () => ({
      language,
      setAppLanguage: (nextLanguage: string) => setLanguage(normalizeLanguage(nextLanguage)),
      t: (key, values) => interpolate(messages[language][key] ?? messages.IT[key], values)
    }),
    [language]
  );

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
}

export function useI18n(): I18nContextValue {
  const context = useContext(I18nContext);
  if (!context) {
    throw new Error("useI18n must be used inside I18nProvider");
  }
  return context;
}

